    <p style="font-size: 14px; text-align: justify;"><b><font color="#0da6ff">PROPOSTA DE REDAÇÃO Nº 2</font></b><br>
    <font color="#000">Com base na leitura do texto motivador e nos conhecimentos construídos ao longo de sua formação pessoal, redija um texto dissertativo-argumentativo em norma culta escrita da língua portuguesa sobre o tema <b>“O sedentarismo e suas consequências”.</b><br>Selecione, organize e relacione, de forma coerente e coesa, argumentos e fatos para defesa de seu ponto de vista.</font></p>
    
    <p style="font-size: 14px; text-align: justify;">
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;“A obesidade, um fator que afeta quase um terço da população mundial, tem várias de suas raízes ligadas ao sedentarismo. Com os avanços da tecnologia, máquinas que preparam comidas, aparelhos que ajudam a emagrecer ou medicamentos que dizem ter os mesmos efeitos, trazem às pessoas muitos riscos, já que em muitos casos não cumprem com o que prometem, mas comprometem a saúde do paciente.”<br>
