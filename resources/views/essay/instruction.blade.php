    <p style="font-size: 14px; text-align: justify;"><font color="#ff2e34"><b>PROCESSO SELETIVO – 2020/2</b></font><br>
    
    <font color="#000">O Processo Seletivo 2020/2 da Faculdade Sogipa propõe dois (2) temas para desenvolvimento de um texto dissertativo argumentativo. <br>O desafio é selecionar dos dois textos propostos, <b>somente um (1)</b> para produzir seu texto.</font></p>
    
    <p style="font-size: 14px; text-align: justify;"><b><font color="#ff2e34">INSTRUÇÕES INICIAIS</font></b><br>
    
    <font color="#000">
    1. Confira o número de páginas do caderno de redação, esse deve conter sete (07) páginas no total. Caso o caderno não esteja completo, solicite a troca com o fiscal.<br>
    2. O texto elaborado deve conter de vinte (20) linhas, quantidade mínima, a trinta e cinco (35) linhas, quantidade máxima, e ser redigido com a letra legível. Se ilegível a redação não será considerada.<br>
    3. Existe, à disposição, a <b>Ata de Ocorrência</b> para registro de quaisquer anormalidades ou comentários considerados importantes pelo candidato para melhoria do Processo. Se necessário, solicite tal documento ao fiscal.</font></p>
    
    <p style="font-size: 14px; text-align: justify;"><b><font color="#ff2e34">INFORMAÇÕES BÁSICAS</font></b><br>
    
    <font color="#000">1.	O tempo de duração da prova é de, no mínimo, duas (2) horas contadas a partir do acesso do link da redação .<br>
    
    2.	 O valor máximo atribuído à redação é de cem (100) pontos. <b>Caso obtenha uma pontuação inferior a vinte (20) pontos, sua redação automaticamente será desclassificada</b>.<br>
    3.	 A redação é avaliada obedecendo aos seguintes parâmetros:<br>
    a) estrutura e conteúdo;<br>
    b) correção gramatical, de acordo com a norma vigente.</font></p>
    
    <p style="font-size: 14px; text-align: justify;"><b><font color="#ff2e34">INSTRUÇÕES PARA A PROVA DE REDAÇÃO</font></b><br>
    
    <font color="#000">1. Leia com atenção os temas propostos a seguir, e <b><u>escolha somente um deles</u></b> para elaborar a sua redação: <br>
    
    <b>• Cuidados com a saúde podem prolongar a vida;<br>
    • O sedentarismo e suas consequências;</b><br>
    
    2.	Atenha-se ao tema escolhido; não misture temas diferentes.<br>
    3.	Produza, sobre o tema escolhido, um texto DISSERTATIVO-ARGUMENTATIVO, ou seja: um texto em que deverá <u>expor o tema proposto e defender seu ponto de vista a respeito dele</u>.<br>
    4.	Certifique-se de que seu texto enquadre-se na tipologia textual solicitada; eventuais trechos narrativos ou descritivos devem ser pequenos e estar a serviço da defesa das ideias apresentadas.<br>
    5.	Não copie trechos dos textos motivadores; seja autor de seu próprio texto.<br>
    6.	Dê um título a seu texto.<br>
    7.	Respeite margens e entradas de parágrafo e utilize a norma culta da língua portuguesa.<br>
    8.	Desenvolva o texto dentro do limite de 20 a 35 linhas.<br>
    9.	Após a elaboração da sua redação você irá encaminhar a mesma em <b>formato PDF</b>.</font></p>
    
    <p style="font-size: 14px; text-align: justify;"><b><font color="#0da6ff">PROPOSTA DE REDAÇÃO Nº 1</font></b><br>
    <font color="#000">COM BASE NA LEITURA DO TEXTO ABAIXO E NAS SUAS REFLEXÕES ACERCA DO QUE ELE REPRESENTA, ELABORE UM TEXTO DISSERTATIVO-ARGUMENTATIVO EM QUE DEVERÁ TRATAR DO SEGUINTE TEMA: <b>“CUIDADOS COM A SAÚDE PODEM MELHORAR A VIDA”</font></b></b></p><br>
    
    <p style="font-size: 14px; text-align: justify;">
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;“Todo mundo sabe que não fumar, fazer esporte, cuidar da alimentação, não exagerar no álcool e evitar o sobrepeso são hábitos saudáveis. Mas nem todos levam essas recomendações ao pé da letra. Um grupo de cientistas da Escola de Saúde Pública Harvard T.H. Chan publicou na revista <b><i>Circulation</i></b> um estudo que deixa mais claros os benefícios de um estilo de vida. Segundo a pesquisa, respeitar essas cinco normas pode representar um aumento da expectativa de vida de até 14 anos para as mulheres e 12 anos para os homens, em relação a quem não os segue. Os autores chegaram a essa conclusão após observarem ao longo de 34 anos os comportamentos mais ou menos saudáveis de 123.000 norte-americanos.”<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;“Não seguir os fatores indicados no estudo pode levar a sérias consequências para a saúde. Os pesquisadores apontam para uma longa lista de efeitos negativos para cada um deles, como o risco de sofrer câncer ou outras doenças causadas pelo tabagismo ou pelo diabetes e os problemas cardiovasculares associados à má alimentação. Por isso insistem que, se esses cinco hábitos forem adotados, os benefícios são garantidos.”</p><br>
    
